int		ft_iterative_factorial(int nb)
{
	int f;
	int i;

	f = nb;
	i = nb - 1;
	if ((nb > 12) || (nb < 0))
		return (0);
	if (nb == 0)
		return (1);
	while (i > 0)
	{
		f = f * i;
		i--;
	}
	return (f);
}
